    private void preencherTabela() {
                ProdutosDAO produtosDAO  = new ProdutosDAO ();
            
                List<ProdutosDTO> listaproduto =  produtosDAO.listarProdutosVendidos();
            
                DefaultTableModel tabelaproduto = (DefaultTableModel) tblVendas1.getModel();
                      
                for (ProdutosDTO c : listaproduto) { 
                    Object[] obj = new Object[] { 
                        c.getId(),            
                        c.getNome(),   
                        c.getStatus(),
                        c.getValor(),         
                    };
                    tabelaproduto.addRow(obj);
                }    
            }
